`dashes` <- function() {
    return(c("\u002d", "\u2013"))
}

`tildae` <- function() {
    return(c("\u007e", "\u223c", "\u00ac", "\u223d"))
}

`singlequotes` <- function() {
    return(c("\u00b4", "\u0060", "\u2018", "\u2019"))
}

`doublequotes` <- function() {
    return(c("\u201c", "\u201d"))
}

`spaces` <- function() {
    return("\u00a0") # multibyte space
}

